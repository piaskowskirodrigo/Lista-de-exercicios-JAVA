package ClassesUtilitarias;

public class ClasseMath {
	public static void main(String[] args) {
		
		System.out.println(Math.pow(2, 3)); //eleva o primeiro pelo segundo
		
		System.out.println(Math.round(4.7)); //arredonda
		
		System.out.println(Math.round(4.4));//arredonda
		
		System.out.println(Math.ceil(4.4)); //arredonda para cima
		
		System.out.println(Math.ceil(4.7)); //arredonda
		
		System.out.println(Math.floor(4.4)); //arredonda para baixo
		
		System.out.println(Math.floor(4.7)); //arredonda para baixo
		
		System.out.println(Math.round(Math.random() * 100)); // o metodo roud arredonda e o random gera um aleatorio ate 0 100
		
		System.out.println(Math.sin(Math.toRadians(30))); //sin = seno
		
		System.out.println(Math.cos(Math.toRadians(1)));//cos = coseno
		
		System.out.println(Math.tan(Math.toRadians(45))); //tan = tangente

	}

}